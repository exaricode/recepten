<header>
    <h1>Bladerdeeghapjes in kerstboomvorm met pesto</h1>
    <p>Deze bladerdeeghapjes met pesto zijn perfect om uit te delen!<p>
</header>

<main>
    <article id="ingredients">
        <h2> 12 porties</h2>
        <ul>
            <li>270 g rollen vers bladerdeeg</li>
            <li>50 g groene pesto</li>
            <li>50 g AH Biologisch geraspte belegen kaas</li>
        </ul>
    </article>

    <article id="necesarry">
        <h2>Dit heb je nodig</h2>
        <ul>
            <li>bakplaten</li>
            <li>bakpapier</li>
            <li>sateprikkers</li>
        </ul>
    </article>

    <article id="preperation">
        <ul>
            <li>
                Verwarm de oven voor op 180 Â°C.
            </li>
            <li>
                Rol het bladerdeeg uit en snijd (voor 12 porties) in de lengte in 12 plakken van 2 cm breed.
            </li>
            <li>
                Vouw zigzaggend een reep op een met bakpapier beklede bakplaat in steeds kleinere vouwen van 
                onder naar boven tot een kerstboompje. Je hebt in totaal 2 bakplaten nodig of bak ze in delen.
            </li>
            <li>
                Steek de prikker (geweekt in water) vanaf het bovenste puntje door het kerstboompje zonder het 
                deeg door te prikken. Zorg ervoor dat de prikker aan de bovenkant er niet te veel uitsteekt. 
                Druk het deeg tegen de prikker aan.
            </li>
            <li>
                Besmeer met de pesto en bestrooi met de kaas. Bak in het midden van de oven in ca. 15 min. gaar.
            </li>
        </ul>
    </article>

    <article>
        <h2>Voedingswaarden (portie)</h2>
